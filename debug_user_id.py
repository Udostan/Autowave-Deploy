        # Test multiple calls with same session
        session_id_2 = session.get('_id', 'anonymous')
        namespace_2 = uuid.UUID('12345678-1234-5678-1234-123456789abc')
        new_user_id_2 = str(uuid.uuid5(namespace_2, f"temp_user_{session_id_2}"))
        
