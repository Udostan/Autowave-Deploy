    hash_user_id = f"temp_user_{hash(test_session.get('_id', 'anonymous'))}"
    
    # UUID method (new)
    uuid_user_id = str(uuid.uuid5(namespace, f"temp_user_{test_session.get('_id', 'anonymous')}"))
    
